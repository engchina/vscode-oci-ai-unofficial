const fs = require("fs");
const path = require("path");

const rootDir = path.resolve(__dirname, "..");
const packageJsonPath = path.join(rootDir, "package.json");
const metadataPath = path.join(rootDir, "src", "config", "runtimeSettings.metadata.json");
const uiSchemaPath = path.join(rootDir, "src", "config", "runtimeSettings.ui-schema.json");
const webviewGeneratedRuntimeSettingsPath = path.join(
  rootDir,
  "webview-ui",
  "src",
  "generated",
  "runtimeSettings.ts",
);

const MAIN_GROUP_TITLE = "oci-ai-unofficial";
const RUNTIME_GROUP_TITLE = "oci-ai-unofficial / Agent / MCP Runtime";

const metadata = JSON.parse(fs.readFileSync(metadataPath, "utf8"));
const uiSchema = JSON.parse(fs.readFileSync(uiSchemaPath, "utf8"));
const packageJson = JSON.parse(fs.readFileSync(packageJsonPath, "utf8"));

const configuration = Array.isArray(packageJson?.contributes?.configuration)
  ? packageJson.contributes.configuration
  : [packageJson.contributes.configuration].filter(Boolean);

if (configuration.length === 0) {
  throw new Error("package.json is missing contributes.configuration");
}

const mainGroup = findOrCreateGroup(configuration, MAIN_GROUP_TITLE);
const runtimeGroup = findOrCreateGroup(configuration, RUNTIME_GROUP_TITLE);

for (const setting of metadata) {
  const propertyKey = `ociAi.${setting.key}`;
  const propertyValue = {
    type: "number",
    default: setting.defaultValue,
    minimum: setting.min,
    maximum: setting.max,
    description: setting.description,
  };

  if (setting.packageGroup === "runtime") {
    delete mainGroup.properties[propertyKey];
    runtimeGroup.properties[propertyKey] = propertyValue;
  } else {
    delete runtimeGroup.properties[propertyKey];
    mainGroup.properties[propertyKey] = propertyValue;
  }
}

packageJson.contributes.configuration = configuration;
fs.writeFileSync(packageJsonPath, `${JSON.stringify(packageJson, null, 2)}\n`);
fs.mkdirSync(path.dirname(webviewGeneratedRuntimeSettingsPath), { recursive: true });
fs.writeFileSync(
  webviewGeneratedRuntimeSettingsPath,
  buildWebviewRuntimeSettingsModule(metadata, uiSchema),
);

function findOrCreateGroup(configurationGroups, title) {
  let group = configurationGroups.find((entry) => entry && entry.title === title);
  if (!group) {
    group = { title, properties: {} };
    configurationGroups.push(group);
  }
  if (!group.properties || typeof group.properties !== "object") {
    group.properties = {};
  }
  return group;
}

function buildWebviewRuntimeSettingsModule(settingsMetadata, runtimeUiSchema) {
  const specsObjectLiteral = settingsMetadata
    .map((setting) => {
      const body = [
        `defaultValue: ${JSON.stringify(setting.defaultValue)}`,
        `min: ${JSON.stringify(setting.min)}`,
        `max: ${JSON.stringify(setting.max)}`,
        ...(setting.step === undefined ? [] : [`step: ${JSON.stringify(setting.step)}`]),
        `kind: ${JSON.stringify(setting.kind)}`,
        ...(setting.uiLabel === undefined ? [] : [`uiLabel: ${JSON.stringify(setting.uiLabel)}`]),
        ...(setting.uiHelpText === undefined ? [] : [`uiHelpText: ${JSON.stringify(setting.uiHelpText)}`]),
        ...(setting.uiPlaceholder === undefined ? [] : [`uiPlaceholder: ${JSON.stringify(setting.uiPlaceholder)}`]),
      ].join(", ");
      return `  ${setting.key}: { ${body} }`;
    })
    .join(",\n");

  const defaultsObjectLiteral = settingsMetadata
    .map((setting) => `  ${setting.key}: ${JSON.stringify(setting.defaultValue)}`)
    .join(",\n");

  return `// This file is auto-generated by scripts/sync-runtime-settings.js.\n` +
    `// Do not edit manually; update src/config/runtimeSettings.metadata.json instead.\n\n` +
    `export const runtimeSettingSpecs = {\n${specsObjectLiteral}\n} as const;\n\n` +
    `export const runtimeSettingDefaults = {\n${defaultsObjectLiteral}\n} as const;\n\n` +
    `export const runtimeSettingsUiSchema = ${JSON.stringify(runtimeUiSchema, null, 2)} as const;\n\n` +
    `export type RuntimeSettingKey = keyof typeof runtimeSettingSpecs;\n`;
}
